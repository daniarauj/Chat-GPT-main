function Area () {
    let r=Number(prompt("Informe o valor do raio do circulo"))
    let pi=Math.PI
    let resultado=r**2*pi
    alert("o resultado da área é "+resultado)
    document.getElementById('a_ret').innerHTML=resultado
}

